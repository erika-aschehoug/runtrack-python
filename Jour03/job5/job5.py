# ➔ Le premier, “num1”, est un nombre,
# ➔ Le deuxième, "operator", est un caractère (string) contenant le type d’opération(+,
# -, *, /, %),
# ➔ Le troisième, “num2”, est un nombre.

# La fonction doit retourner le résultat de l’opération.

